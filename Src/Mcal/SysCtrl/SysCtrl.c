void SysCtrl_Init(void)
{
	RCC|= ( (UserClockConfig.clockSource&3) <<4 );
	if(UserClockConfig.clockSource == SysCtrl_Main_Oscillator)//enable main oscillator
		RCC&= (~1);
	//bybass
	if(UserClockConfig.pllState == SysCtrl_PLL_On)
	{
		RCC &= ~(1<<11);
		RCC &= ~(1<<13);
	}
		
	else
	{
		RCC |= (1<<11);
		RCC |= (1<<13);
	}
	/* clock divisor */
	if(UserClockConfig.clockDivider==SysCtrl_Clock_DibidedBy_1)
		RCC &= ~(1<<22);
	else
	{
		RCC |= (1<<22);
		//form sysdiv1
		if(UserClockConfig.clockDivider<=SysCtrl_Clock_DibidedBy_16)
		{
			RCC&= ~(0xf<<23);
			RCC|= (UserClockConfig.clockDivider<<23);
		}
	}
}

void SysCtrl_EnablePeripheralClock(SysCtrl_PeripheralClock PeripheralClock)
{
	if(PeripheralClock<= SysCtrl_Timer5_16_23_Clock)
		RCGCTIMER |= (1<<PeripheralClock);
	else if (PeripheralClock<= SysCtrl_GPIO_PORTF_Clock)
		RCGCGPIO |= (1<<(PeripheralClock-6));
	
		
}

